// VARIABLES:

// 'var' keyword is used to make variables.
// prompt() method is used to take input from the user.
var userName = prompt("What is your name? ");
alert("Hello " + userName + ".");

// Updating the value of variables
var level = 1;
alert("level " + level);

level = 2;
alert("level " + level);

level = 3;
alert("level " + level);

