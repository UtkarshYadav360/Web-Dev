// BASIC ARITHMETIC OPERATORS:
// 1) Addition +
var sum = 5 + 3;
console.log("Addition (5 + 3) " + sum);

// 2) Subtraction -
var sub = 5 - 3;
console.log("Subtraction (5 - 3) : " + sub);

// 3) Multiplication *
var multiply = 5 * 3;
console.log("Multiplication (5 * 3) : " + multiply);

// 4) Division /
var divide = 5 / 3;
console.log("Division (5 / 3) : " + divide);

// 6) Modulo %
var modulo = 5 % 3;
console.log("Modulo (5 % 3) : " + modulo);

