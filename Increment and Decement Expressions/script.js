// INCREMENT AND DECREMENT EXPRESSIONS:
// ++ is used to add 1 to a number.
var x = 5;
alert("Before Increment Expression : " + x);
x++;
alert("After Increment Expression : " + x);
x += 3;
alert("After Adding three : " + x);

// -- is used to add 1 to a number.
x--;
alert("After Decrement Expression : " + x);
x -= 5;
alert("After Subtracting five : " + x);
